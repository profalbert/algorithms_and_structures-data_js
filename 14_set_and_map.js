// Словарь, его также называют: карта, мап

// Map
// Объект Map содержит пары ключ-значение и сохраняет порядок вставки.
// Любое значение (как объекты, так и примитивы) могут быть
// использованы в качестве ключей.
const map = new Map()
const objKey = { id: 5 }
map.set(objKey, 'Albert')
map.set(objKey, 'Albert1')
console.log('Map: достаем по ключу значение из Map: ', map.get(objKey))

